#include <unistd.h>

void	ft_is_negative(int n)
{
	char	np;
	char	nn;

	np = 'P';
	nn = 'N';
	if (n >= 0)
	{
		write (1, &np, 1);
	}
	else
	{
		write (1, &nn, 1);
	}
}
