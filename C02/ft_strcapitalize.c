char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + 32;
		i++;
	}
	return (str);
}

char	*ft_strcapitalize(char *str)
{
	int	i;
	int	fg;

	fg = 1;
	i = 0;
	ft_strlowcase(str);
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			if (fg == 1)
				str[i] = str[i] - 32;
			fg = 0;
		}
		else if (str[i] >= '0' && str[i] <= 9)
			fg = 0;
		else
			fg = 1;
		i++;
	}
	return (str);
}
